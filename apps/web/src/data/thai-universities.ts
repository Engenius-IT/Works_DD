export const getThaiUniversities = (locale: 'th' | 'en' = 'th') => {
    return THAI_UNIVERSITIES.map((item) => {
        if (locale === 'en') {
            return UNIVERSITY_EN_MAP[item] || item;
        }
        return item;
    }).sort((a, b) => {
        if (locale === 'en') {
            if (a === 'Other') return 1;
            if (b === 'Other') return -1;
            return a.localeCompare(b, 'en');
        } else {
            if (a === 'อื่นๆ') return 1;
            if (b === 'อื่นๆ') return -1;
            return a.localeCompare(b, 'th');
        }
    });
};